<script>
    function click_myTopnav() {
        var el = document.getElementById("myTopnav");
        if (el.className === "topnav") {
            el.className += " responsive";
        } else {
            el.className = "topnav";
        }
    }

    function click_myTabnav() {
        var el = document.getElementById("myTabnav");
        if (el.className === "topnav") {
            el.className += " responsive";
        } else {
            el.className = "topnav";
        }
    }
</script>
